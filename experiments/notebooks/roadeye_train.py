from pathlib import Path
import yaml

root = Path(dataset.location)

# rewrite all label files so every object becomes class 0 ("plate")
for split in ["train", "valid", "test"]:
    label_dir = root / split / "labels"
    if not label_dir.exists():
        continue

    for label_file in label_dir.glob("*.txt"):
        new_lines = []
        for line in label_file.read_text().strip().splitlines():
            parts = line.split()
            if len(parts) < 5:
                continue
            parts[0] = "0"   # class id -> plate
            new_lines.append(" ".join(parts))
        label_file.write_text("\n".join(new_lines) + ("\n" if new_lines else ""))

# fix data.yaml
data_yaml_path = root / "data.yaml"
with open(data_yaml_path, "r") as f:
    data = yaml.safe_load(f)

data["nc"] = 1
data["names"] = ["plate"]

with open(data_yaml_path, "w") as f:
    yaml.safe_dump(data, f, sort_keys=False)

print("Converted to single-class plate dataset.")

print("Dataset path:", dataset.location)
print(dataset)

from ultralytics import YOLO

model = YOLO("yolov8s.pt")

results = model.train(
    data=str(data_yaml_path),
    epochs=80,
    imgsz=640,
    batch=16,
    patience=20,
    device=0,
    project="/content/runs",
    name="roadeye_plate_single_class",
    fliplr=0.0,
    flipud=0.0,
    degrees=3.0,
    translate=0.08,
    scale=0.3,
    hsv_v=0.5,
    hsv_s=0.6,
    mosaic=0.5,
    erasing=0.2,
    optimizer="AdamW",
    lr0=0.001,
    cos_lr=True,
    plots=True,
)

print("Training complete!")
print(f"Best mAP50: {results.results_dict.get('metrics/mAP50(B)')}")

