def yolo_line(pred, img_w, img_h):
    class_name = (
        pred.get("class")
        or pred.get("class_name")
        or pred.get("label")
    )

    if class_name not in CLASS_TO_ID:
        return None

    x = float(pred["x"])
    y = float(pred["y"])
    w = float(pred["width"])
    h = float(pred["height"])

    x_center = x / img_w
    y_center = y / img_h
    width = w / img_w
    height = h / img_h

    # Clamp to valid YOLO range
    x_center = min(max(x_center, 0), 1)
    y_center = min(max(y_center, 0), 1)
    width = min(max(width, 0), 1)
    height = min(max(height, 0), 1)

    return f"{CLASS_TO_ID[class_name]} {x_center:.6f} {y_center:.6f} {width:.6f} {height:.6f}"

def extract_predictions(workflow_result):
    """
    Handles common Roboflow Workflow output shapes.
    """
    if isinstance(workflow_result, list):
        workflow_result = workflow_result[0]

    preds = workflow_result.get("predictions", workflow_result)

    if isinstance(preds, dict) and "predictions" in preds:
        return preds["predictions"]

    if isinstance(preds, list):
        return preds

    return []

def assign_split(files):
    random.shuffle(files)

    n = len(files)
    train_end = int(n * SPLITS["train"])
    valid_end = train_end + int(n * SPLITS["valid"])

    return {
        "train": files[:train_end],
        "valid": files[train_end:valid_end],
        "test": files[valid_end:],
    }

def main():
    image_files = [
        p for p in INPUT_DIR.glob("*")
        if p.suffix.lower() in [".jpg", ".jpeg", ".png", ".bmp", ".webp"]
    ]

    if not image_files:
        raise ValueError(f"No images found in {INPUT_DIR}")

    for split in SPLITS:
        (OUTPUT_DIR / split / "images").mkdir(parents=True, exist_ok=True)
        (OUTPUT_DIR / split / "labels").mkdir(parents=True, exist_ok=True)

    split_files = assign_split(image_files)

    for split, files in split_files.items():
        for img_path in tqdm(files, desc=f"Annotating {split}"):
            with Image.open(img_path) as im:
                img_w, img_h = im.size

            result = client.run_workflow(
                workspace_name=WORKSPACE,
                workflow_id=WORKFLOW_ID,
                images={
                    "image": str(img_path)
                },
                parameters={
                    "confidence": CONFIDENCE,
                    "iou_threshold": IOU_THRESHOLD,
                    "class_agnostic_nms": False,
                    "max_detections": 1000,
                },
                use_cache=True,
            )

            predictions = extract_predictions(result)

            label_lines = []
            for pred in predictions:
                line = yolo_line(pred, img_w, img_h)
                if line is not None:
                    label_lines.append(line)

            out_img = OUTPUT_DIR / split / "images" / img_path.name
            out_label = OUTPUT_DIR / split / "labels" / f"{img_path.stem}.txt"

            shutil.copy2(img_path, out_img)
            out_label.write_text("\n".join(label_lines))

    data_yaml = {
        "path": str(OUTPUT_DIR.resolve()),
        "train": "train/images",
        "val": "valid/images",
        "test": "test/images",
        "names": {i: name for i, name in enumerate(CLASSES)},
    }

    with open(OUTPUT_DIR / "data.yaml", "w") as f:
        yaml.safe_dump(data_yaml, f, sort_keys=False)

    print(f"Done. YOLO dataset written to: {OUTPUT_DIR.resolve()}")

if __name__ == "__main__":
    main()

