def merge_split(split: str, sources: list[Path], extra: Path | None = None):
    all_sources = list(sources)
    if extra and (extra / split).exists():
        all_sources.append(extra)

    for src in all_sources:
        img_dir = src / split / "images"
        lbl_dir = src / split / "labels"
        if not img_dir.exists():
            continue
        for img in img_dir.glob("*.*"):
            lbl = lbl_dir / (img.stem + ".txt")
            dst_img = COMBINED / split / "images" / f"{src.name}_{img.name}"
            dst_lbl = COMBINED / split / "labels" / f"{src.name}_{img.stem}.txt"
            shutil.copy2(img, dst_img)
            if lbl.exists():
                shutil.copy2(lbl, dst_lbl)

